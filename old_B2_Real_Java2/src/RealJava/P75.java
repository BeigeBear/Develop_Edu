package RealJava;

public class P75 {

//	이게 진짜 자바 p75
	public static void main(String[] args) {
		int a = 1;
		a += 1;    // a = a + 1
		a -= 1;    // a = a - 1
		a *= 10;   // a = a * 10;
		a /= 10;   // a = a / 10;
		a %= 2;    // a = a % 2;
		a ^= 3;    // a = a & 3;
		a |= 3;    // a = a | 3;
		a <<= 2;
	}

}
