package Quiz;

// 입력한 값의 개수만큼 '*'를 표시하는 프로그램을 작성하자. 마지막에는 줄바꿈 문자를 출력할 것.
// 단, 읽은 값이 1미만이면 줄 바꿈 문자를 표시해서는 안 된다.

import java.util.Scanner;

public class Quiz_4_7 {

	public static void main(String[] args) {
		Scanner star=new Scanner(System.in);
		System.out.println("숫자를 입력하세요");
		
		int n = star.nextInt();     		// n을 입력
		
		if (n>0) { 							// 입력한 n이 0보다 크면
			int i = 0;						// i 를 0으로 선언
			while(i<n) {					// i가 n보다 작을 경우
				System.out.print("*");		// * 삽입
				i++;						// i를 1씩 상승
			}
		}
		System.out.println();
	}
}